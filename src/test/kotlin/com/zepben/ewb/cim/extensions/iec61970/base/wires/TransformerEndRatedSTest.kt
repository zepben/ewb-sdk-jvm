package com.zepben.ewb.cim.extensions.iec61970.base.wires

import com.zepben.testutils.junit.SystemLogExtension
import org.hamcrest.MatcherAssert.assertThat
import org.hamcrest.Matchers.equalTo
import org.junit.jupiter.api.Test
import org.junit.jupiter.api.extension.RegisterExtension

internal class TransformerEndRatedSTest {

    @JvmField
    @RegisterExtension
    var systemErr: SystemLogExtension = SystemLogExtension.SYSTEM_ERR.captureLog().muteOnSuccess()

    @Test
    internal fun accessorCoverage() {
        val batteryControl = TransformerEndRatedS(TransformerCoolingType.KDAF, 100)

        assertThat(batteryControl.coolingType, equalTo(TransformerCoolingType.KDAF))
        assertThat(batteryControl.ratedS, equalTo(100))
    }

}
